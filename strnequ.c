#include "libft.h"

int			ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	k;

	k = 0;
	while (s1 && s2 && k <= n)
	{
		if (s1[k] != s2[k])
			return (0);
		k++;
	}
	return (1);
}
