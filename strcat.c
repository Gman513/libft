char	*ft_strcat(char *s1, const char *s2)
{
	int	k;
	int	l;
	int	len;

	k = 0;
	l = 0;
	len = 0;
	while (s1[len] != '\0')
		len++;
	k = len;
	while (s2[l] != '\0' && l <= len)
	{
		s1[k] = s2[l];
		k++;
		l++;
	}
	s1[k + 1] = '\0';
	return (s1);
}
