#include "libft.h"

size_t	ft_putnbr(int n)
{
	size_t	len;

	len = 0;
	if (n < 0)
	{
		n = -n;
		len += ft_putchar('-');
	}
	if (n >= 10)
	{
		len += ft_putnbr(n / 10);
	}
	len += ft_putchar(n % 10 + '0');
	return (len);
}
