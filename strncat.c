#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		k;
	size_t	l;
	size_t	len;

	l = 0;
	len = 0;
	while (s1[len] != '\0')
		len++;
	k = len;
	while (s2[l] != '\0' && l < n - 1)
	{
		s1[k] = s2[l];
		k++;
		l++;
	}
	s1[k + 1] = '\0';
	return (s1);
}
